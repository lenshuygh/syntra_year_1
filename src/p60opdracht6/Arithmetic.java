package p60opdracht6;

public class Arithmetic {
    public static void main(String[] args) {
        int num1 = 8;
        int num2 = -6;
        System.out.print(num1+" < " + num2 + "\t ? : ");
        System.out.println(num1 < num2);

        System.out.print(num1+" <= " + num2 + "\t ? : ");
        System.out.println(num1 <= num2);

        System.out.print(num1+" > " + num2 + "\t ? : ");
        System.out.println(num1 > num2);

        System.out.print(num1+" >= " + num2 + "\t ? : ");
        System.out.println(num1 >= num2);

        System.out.print(num1+" == " + num2 + "\t ? : ");
        System.out.println(num1 == num2);

        System.out.print(num1+" != " + num2 + "\t ? : ");
        System.out.println(num1 != num2);
    }
}
